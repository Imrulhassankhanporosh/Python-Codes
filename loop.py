summation = 0
i = 1
while i <= 5:
    summation = summation + i
    print("When i is : ", i, "sum is :  ", summation)
    i = i + 1

print("total sum :", summation)

# While loop

summation = 0
i = 1
x = [1, 3, 4, 6, 2]
for i in x:
    summation = summation + i
    print("When i is : ", i, "sum is :  ", summation)
    i = i + 1

print("total sum :", summation)
